typedef long long                           ll;
typedef vector<ll>                          vll;
typedef pair<ll, ll>                        pll;
typedef vector<vector<ll>>                  vvl;
typedef vector<pll>                         vpl;
typedef pair<int, int>                      pii;
typedef long double                         ld;
typedef tree<ll, null_type, less<ll>, rb_tree_tag, tree_order_statistics_node_update> oset;




void solve() {
	ll n , q;
	cin >> n >> q;
	vector<long long > arr(n);

	map <ll, oset> cnt;
	for (int i = 0; i < n; i++) {
		cin >> arr[i];
		cnt[arr[i]].insert(i);
	}



	while (q--) {
		ll t, a , b;
		cin >> t >> a >> b;
		if (t == 1) {
			a--;
			ll current = arr[a];
			cnt[current].erase(a);
			cnt[b].insert(a);

		} else if (t == 2) {
			a--; b--;
			ll ans = 0;
			for (auto &e : cnt) {
				auto curr = e.second;
				ll r = curr.order_of_key(b + 1);
				ll l = curr.order_of_key(a);
				ll cnt = r - l;
				if (cnt % 2) ans++;
			}
			cout << ans << " ";
		}
	}
	cout << endl;
}

int main() {
	int tt = 1;
	cin >> tt; // "UN-COMMENT THIS FOR TESTCASES"
	while (tt--) {
		solve();
	}
}