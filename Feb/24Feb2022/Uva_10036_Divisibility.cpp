const int mxn = 1e4 + 10;
int dp[mxn][110];

int solve(vector<int> &arr, int curr_mod, int K, int i = 0) {
	if (i == arr.size()) {
		return curr_mod == 0;
	}

	int &ans = dp[i][curr_mod];
	if (ans != -1) return ans;

	ans = 0;
	ans = solve(arr, ((curr_mod + arr[i]) % K + K ) % K, K, i + 1);
	ans |= solve(arr, ((curr_mod - arr[i]) % K + K) % K, K, i + 1);
	return ans;
}



int main() {
	int M; cin >> M;
	while (M--) {
		int N , K;
		cin >> N >> K;
		vector<int> arr(N);
		for (auto &a : arr) {
			cin >> a;
		}
		memset(dp, -1, sizeof(dp));

		bool is = solve(arr, 0, K);
		cout << (is ? "Divisible" : "Not divisible") << endl;
	}
}