long long get_inversions(vector<int>arr, int N) {
	long long ans = 0;
	for (int i = 1; i < N - 1; i++) {
		long long current = arr[i];
		long long smaller = 0 , greater = 0;
		for (int j = 0; j < i; j++) {
			if (arr[j] > current) greater++;
		}
		for (int j = i + 1; j < N; j++) {
			if (arr[j] < current) smaller++;
		}
		ans += smaller * greater;
	}
	return ans;
}

int main() {
	int N; cin >> N;
	vector<int> arr(N);
	for (int i = 0; i < N; i++) {
		cin >> arr[i];
	}
	long long ans = get_inversions(arr, N);
	cout << ans << endl;
}