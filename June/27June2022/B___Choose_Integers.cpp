void solve() {
	ll a , b , c;
	cin >> a >> b >> c;

	ll t = c % b;

	set<ll> s;
	ll counter = 1;
	while (s.size() != b) {
		ll curr = (a * counter) % b;
		if (s.count(curr)) break;
		s.insert(curr);
		counter++;
	}
	bool is = s.count(t);
	cout << (is ? "YES" : "NO") << endl;
}
