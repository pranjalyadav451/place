int solve(int start , int end) {
	if (end - start < 0) return 1;
	int ans = 0;
	for (int i = start ; i <= end; i++) {
		int left_sub = solve(start, i - 1) , right_sub = solve(i + 1, end);
		ans += left_sub * right_sub;
	}
	return ans;
}

int numTrees(int A) {
	int ans = solve(1, A);
	return ans;
}

